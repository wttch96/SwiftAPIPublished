import Foundation


enum HttpMethod: String {
    case post = "POST"
    case get = "GET"
    case delete = "DELETE"
    case put = "PUT"
    case options = "OPTIONS"
    case head = "HEAD"
    case connect = "CONNECT"
    case trace = "TRACE"
}
